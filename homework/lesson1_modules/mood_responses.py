def mood_response(mood):
    if mood == "happy":
        print("Your happiness is contagious!")
    elif mood == "sad":
        print("As Bob Ross used to say 'Gotta have a little sadness once in a while so you know when the good times come.'")
    elif mood == "excited":
        print("LETSSS GOOOOOO!!!")
    elif mood == "stressed":
        print("Life is too short my friend. What will be, will be.")
    
